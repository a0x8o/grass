/************************************************************/
// shift n items to the left: in effect this deletes the first n items
template <class T>
void im_buffer<T>::shift_left(unsigned long n)
{
    assert(n <= size);
    // remains sorted
    if (n) {
        memmove(data, data + n, (size - n) * sizeof(T));
        size -= n;
    }
}

