/************************************************************/
// copy constructor
template <class T>
im_buffer<T>::im_buffer(const im_buffer &b)
{

    MEMORY_LOG("im_buffer: copy constructor start\n");
    maxsize = b.maxsize;
    size = b.size;
    sorted = b.sorted;
    assert(data);
    for (unsigned long i = 0; i < size; i++) {
        data[i] = b.data[i];
    }
    MEMORY_LOG("im_buffer: copy constructor end\n");
}

/************************************************************/
// insert an item in buffer; fail if buffer full
template <class T>
bool im_buffer<T>::insert(T &x)
{

    if (size == maxsize) {
        return false; // buffer full
    }
#ifdef SAVE_MEMORY
    if (!data) {
        data = new T[maxsize];
    }
#endif
    assert(data);
    assert(size < maxsize);
    data[size] = x;
    size++;
    sorted = false; // not worth checking..
    return true;
}

