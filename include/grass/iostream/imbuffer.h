template <class T>
unsigned long im_buffer<T>::insert(T *x, unsigned long n)
{

    assert(x);
    for (unsigned long i = 0; i < n; i++) {
        if (!insert(x[i])) {
            return i;
        }
    }
    assert(sorted == false);
    return n;
}

