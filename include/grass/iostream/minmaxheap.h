template <class T>
void BasicMinMaxHeap<T>::trickleDownMin(HeapIndex i)
{
    HeapIndex m;
    bool done = false;

    while (!done) {

        if (!hasChildren(i)) {
            done = true;
            return;
        }
        m = smallestChildGrandchild(i);
        if (isGrandchildOf(i, m)) {
            if (A[m] < A[i]) {
                swap(i, m);
                if (A[m] > A[parent(m)]) {
                    swap(m, parent(m));
                }
                // trickleDownMin(m);
                i = m;
            }
            else {
                done = true;
            }
        }
        else {
            if (A[m] < A[i]) {
                swap(i, m);
            }
            done = true;
        }
    } // while
}

