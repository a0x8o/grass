template <class T>
HeapIndex BasicMinMaxHeap<T>::largestChild(HeapIndex i) const
{
    assert(hasChildren(i));
    if (hasRightChild(i) && (leftChildValue(i) < rightChildValue(i))) {
        return rightChild(i);
    }
    else {
        return leftChild(i);
    }
}

