template <class T>
void BasicMinMaxHeap<T>::trickleDown(HeapIndex i)
{
    if (isOnMinLevel(i)) {
        trickleDownMin(i);
    }
    else {
        trickleDownMax(i);
    }
}

// ----------------------------------------------------------------------
template <class T>
void BasicMinMaxHeap<T>::bubbleUp(HeapIndex i)
{
    HeapIndex m;
    m = parent(i);

    if (isOnMinLevel(i)) {
        if (m && (A[i] > A[m])) {
            swap(i, m);
            bubbleUpMax(m);
        }
        else {
            bubbleUpMin(i);
        }
    }
    else {
        if (m && (A[i] < A[m])) {
            swap(i, m);
            bubbleUpMin(m);
        }
        else {
            bubbleUpMax(i);
        }
    }
}

