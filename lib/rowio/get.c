void Rowio_flush(ROWIO *R)
{
    int i;

    for (i = 0; i < R->nrows; i++)
        pageout(R, i);
}

static void pageout(ROWIO *R, int cur)
{
    if (R->rcb[cur].row < 0)
        return;
    if (!R->rcb[cur].dirty)
        return;
    (*R->putrow)(R->fd, R->rcb[cur].buf, R->rcb[cur].row, R->len);
    R->rcb[cur].dirty = 0;
}

static void *my_select(ROWIO *R, int n)
{
    int i;

    R->rcb[n].age = 0;
    for (i = 0; i < R->nrows; i++)
        R->rcb[i].age++;
    R->cur = R->rcb[n].row;
    R->buf = R->rcb[n].buf;
    return R->buf;
}
