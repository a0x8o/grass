/*redefine */
#define TEST_N_NUM_DEPTHS_LOCAL 2
#define TEST_N_NUM_ROWS_LOCAL   3
#define TEST_N_NUM_COLS_LOCAL   3

/* prototypes */
static N_gwflow_data2d *create_gwflow_data_2d(void);
