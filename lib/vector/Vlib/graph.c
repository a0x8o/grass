{
    dglInt32_t dglcosts;

    /* TODO: Not tested! */
    G_debug(3, "Vect_graph_set_node_costs()");

    dglcosts = (dglInt32_t)costs * 1000;

    dglNodeSet_Attr(graph, dglGetNode(graph, (dglInt32_t)node), &dglcosts);
}

