/*!
 * \brief Compute the maximum norm of vector x
 *
 * \f[ a = ||{\bf x}||_\infty \f]
 *
 * This function is not multi-threaded
 *
 * \param x       (double *)-- the vector
 * \param value (double *)  -- the return value
 * \param rows (int)
 * \return (void)
 *
 * */
void G_math_d_max_norm(double *x, double *value, int rows)
{
    int i;

    double max = 0.0;

    max = fabs(x[rows - 1]);
    for (i = rows - 2; i >= 0; i--) {
        if (max < fabs(x[i]))
            max = fabs(x[i]);
    }

    *value = max;
}

/*!
 * \brief Scales vectors x and y with the scalars a and b and adds them
 *
 * \f[ {\bf z} = a{\bf x} + b{\bf y} \f]
 *
 * This function is multi-threaded with OpenMP and can be called within a
 * parallel OpenMP region.
 *
 * \param x      (double *)
 * \param y      (double *)
 * \param z      (double *)
 * \param a      (double)
 * \param b      (double)
 * \param rows (int)
 * \return (void)
 *
 * */
void G_math_d_ax_by(double *x, double *y, double *z, double a, double b,
                    int rows)
{
    int i;

    /*find specific cases */
    if (b == 0.0) {
#pragma omp for schedule(static)
        for (i = rows - 1; i >= 0; i--) {
            z[i] = a * x[i];
        }
    }
    else if ((a == 1.0) && (b == 1.0)) {
#pragma omp for schedule(static)
        for (i = rows - 1; i >= 0; i--) {
            z[i] = x[i] + y[i];
        }
    }
    else if ((a == 1.0) && (b == -1.0)) {
#pragma omp for schedule(static)
        for (i = rows - 1; i >= 0; i--) {
            z[i] = x[i] - y[i];
        }
    }
    else if (a == b) {
#pragma omp for schedule(static)
        for (i = rows - 1; i >= 0; i--) {
            z[i] = a * (x[i] + y[i]);
        }
    }
    else if (b == -1.0) {
#pragma omp for schedule(static)
        for (i = rows - 1; i >= 0; i--) {
            z[i] = a * x[i] - y[i];
        }
    }
    else if (b == 1.0) {
#pragma omp for schedule(static)
        for (i = rows - 1; i >= 0; i--) {
            z[i] = a * x[i] + y[i];
        }
    }
    else {
#pragma omp for schedule(static)
        for (i = rows - 1; i >= 0; i--) {
            z[i] = a * x[i] + b * y[i];
        }
    }

    return;
}

/*!
 * \brief Copy the vector x to y
 *
 * \f[ {\bf y} = {\bf x} \f]
 *
 * This function is not multi-threaded
 *
 * \param x      (double *)
 * \param y      (double *)
 * \param rows (int)
 *
 * */
void G_math_d_copy(double *x, double *y, int rows)
{
    y = memcpy(y, x, rows * sizeof(double));

    return;
}

