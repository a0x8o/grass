int Rast3d_cache_unlock(RASTER3D_cache *c, int name)
{
    int index;

    index = Rast3d_cache_hash_name2index(c->hash, name);
    if (index == -1) {
        Rast3d_error("Rast3d_cache_unlock: name not in cache");
        return 0;
    }

    if (IS_UNLOCKED_ELT(index))
        return 1;

    cache_queue_append(c, index);
    UNLOCK_ELT(index);

    return 1;
}

