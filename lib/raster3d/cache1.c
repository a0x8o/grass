/*---------------------------------------------------------------------------*/

void Rast3d_cache_dispose(RASTER3D_cache *c)
{
    if (c == NULL)
        return;

    Rast3d_cache_hash_dispose(c->hash);

    if (c->elts != NULL)
        Rast3d_free(c->elts);
    if (c->names != NULL)
        Rast3d_free(c->names);
    if (c->locks != NULL)
        Rast3d_free(c->locks);
    if (c->next != NULL)
        Rast3d_free(c->next);
    if (c->prev != NULL)
        Rast3d_free(c->prev);

    Rast3d_free(c);
}

