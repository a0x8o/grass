/* Create and init new node */
SQLPNODE *sqpNewNode(void)
{
    SQLPNODE *np;

    np = (SQLPNODE *)calloc(1, sizeof(SQLPNODE));
    return np;
}

SQLPNODE *sqpNewExpressionNode(int oper, SQLPNODE *left, SQLPNODE *right)
{
    SQLPNODE *np;

    np = sqpNewNode();

    np->node_type = SQLP_NODE_EXPRESSION;
    np->oper = oper;
    np->left = left;
    np->right = right;

    return np;
}

SQLPNODE *sqpNewColumnNode(char *name)
{
    SQLPNODE *np;

    np = sqpNewNode();

    np->node_type = SQLP_NODE_COLUMN;
    np->column_name = strdup(name);

    return np;
}

SQLPNODE *sqpNewValueNode(char *strval, int intval, double dblval, int type)
{
    SQLPNODE *np;

    np = sqpNewNode();

    np->node_type = SQLP_NODE_VALUE;

    np->value.type = type;
    if (strval)
        np->value.s = strdup(strval);
    np->value.i = intval;
    np->value.d = dblval;

    return np;
}

void sqpFreeNode(SQLPNODE *np)
{
    if (!np)
        return;

    if (np->left)
        sqpFreeNode(np->left);

    if (np->right)
        sqpFreeNode(np->right);

    if (np->column_name)
        free(np->column_name);

    if (np->value.s)
        free(np->value.s);

    free(np);
}

int sqpOperatorCode(char *oper)
{
    char *tmp, *ptr;

    /* Convert to lower case */
    tmp = strdup(oper);
    ptr = tmp;
    while (*ptr) {
        *ptr = tolower(*ptr);
        ptr++;
    }

    if (strcmp(oper, "=") == 0)
        return SQLP_EQ;
    else if (strcmp(oper, "<") == 0)
        return SQLP_LT;
    else if (strcmp(oper, "<=") == 0)
        return SQLP_LE;
    else if (strcmp(oper, ">") == 0)
        return SQLP_GT;
    else if (strcmp(oper, ">=") == 0)
        return SQLP_GE;
    else if (strcmp(oper, "<>") == 0)
        return SQLP_NE;
    else if (strcmp(oper, "~") == 0)
        return SQLP_MTCH;
    else if (strcmp(oper, "+") == 0)
        return SQLP_ADD;
    else if (strcmp(oper, "-") == 0)
        return SQLP_SUBTR;
    else if (strcmp(oper, "*") == 0)
        return SQLP_MLTP;
    else if (strcmp(oper, "/") == 0)
        return SQLP_DIV;
    else if (strcmp(oper, "and") == 0)
        return SQLP_AND;
    else if (strcmp(oper, "or") == 0)
        return SQLP_OR;
    else if (strcmp(oper, "not") == 0)
        return SQLP_NOT;

    free(tmp);

    return 0;
}

