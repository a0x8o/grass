def get_desc(cmd):
    with Path(cmd).open() as f:
        while True:
            line = f.readline()
            if not line:
                return ""
            if "NAME" in line:
                break

        while True:
            line = f.readline()
            if not line:
                return ""
            if "SYNOPSIS" in line:
                break
            if "<em>" in line:
                sp = line.split("-", 1)
                if len(sp) > 1:
                    return sp[1].strip()
                return None

