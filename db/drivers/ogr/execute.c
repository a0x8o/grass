int parse_sql_update(const char *sql, char **table, column_info **cols,
                     int *ncols, char **where)
{
    int nprefix, n;
    int has_where;
    char *prefix;
    char *p, *w, *c, *t;
    char **token, **itoken;

    prefix = "UPDATE";
    nprefix = strlen(prefix);
    if (G_strncasecmp(sql, prefix, nprefix) != 0)
        return 1;

    p = (char *)sql + nprefix; /* skip 'UPDATE' */
    if (*p != ' ')
        return 1;
    p++;

    /* table */
    t = strchr(p, ' ');
    n = t - p;
    *table = G_malloc(n + 1);
    strncpy(*table, p, n);
    (*table)[n] = '\0';
    G_strip(*table);

    p += n;
    if (*p != ' ')
        return 1;
    p++;

    if (G_strncasecmp(p, "SET", 3) != 0)
        return 1;

    p += 3; /* skip 'SET' */

    if (*p != ' ')
        return 1;
    p++;

    w = G_strcasestr(p, "WHERE");

    if (!w) {
        has_where = FALSE;
        w = (char *)sql + strlen(sql);
    }
    else {
        has_where = TRUE;
    }

    /* process columns & values */
    n = w - p;
    c = G_malloc(n + 1);
    strncpy(c, p, n);
    c[n] = '\0';

    token = G_tokenize2(c, ",", "'");
    *ncols = G_number_of_tokens(token);
    *cols = (column_info *)G_malloc(sizeof(column_info) * (*ncols));

    for (n = 0; n < (*ncols); n++) {
        G_chop(token[n]);
        itoken = G_tokenize(token[n], "=");
        if (G_number_of_tokens(itoken) != 2)
            return FALSE;
        G_strip(itoken[0]);
        G_strip(itoken[1]);
        (*cols)[n].name = G_store(itoken[0]);
        (*cols)[n].value = G_store(itoken[1]);
        G_free_tokens(itoken);
    }

    G_free_tokens(token);
    G_free(c);

    if (!has_where) {
        *where = NULL;
        return 0;
    }

    /* where */
    w += strlen("WHERE");
    if (*w != ' ')
        return 1;
    w++;

    G_strip(w);
    *where = G_store(w);

    return 0;
}
