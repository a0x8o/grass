/* get the output type */
static int raster_type_option_string_enum(const char *type)
{
    /* this function could go to the library but the exact behavior needs
     * to be figured out */
    if (strcmp("CELL", type) == 0)
        return CELL_TYPE;
    else if (strcmp("DCELL", type) == 0)
        return DCELL_TYPE;
    else
        return FCELL_TYPE;
}

