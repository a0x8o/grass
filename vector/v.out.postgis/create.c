    G_asprintf(&filename, "PG_%d", (int)getpid());
    G_debug(1, "PG file: %s", filename);

    fp = G_fopen_new("", filename);
    if (!fp)
        G_fatal_error(_("Unable to create <%s> file"), filename);
    sprintf(buf, "GRASS_VECTOR_PGFILE=%s", filename);
    putenv(G_store(buf));
