        if (ogrfieldnum > -1)
            OGR_F_UnsetField(Ogr_feature, ogrfieldnum);
        /* doatt reset moved into have cat loop as the table needs to be
           open to know the OGR field ID. Hopefully this has no ill consequences
         */
