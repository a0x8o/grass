/* compare first according to x-coordinate, if equal then y-coordinate */
int cmp(const void *a, const void *b)
{
    struct vertex *p1 = (struct vertex *)a;
    struct vertex *p2 = (struct vertex *)b;

    if (p1->x < p2->x)
        return 1;
    else if (p1->x > p2->x)
        return -1;
    else {
        if (p1->y < p2->y)
            return 1;
        else if (p1->y > p2->y)
            return -1;
    }

    return 0;
}

