static int file_cpy(FILE *from, FILE *to)
{
    char buf[BUFSIZ];
    size_t size;
    int written = 0;

    while (1) {
        size = fread(buf, 1, BUFSIZ, from);
        if (!size) {
            if (written) {
                fflush(to);
                G_fseek(to, 0L, SEEK_SET);
            }
            return (0);
        }
        if (!fwrite(buf, 1, size, to)) {
            G_warning(_("Unable to write to file"));
            return (-1);
        }
        written = 1;
    }

    /* NOTREACHED */
    return -1;
}
