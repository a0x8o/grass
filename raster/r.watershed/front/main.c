static void do_opt(const struct Option *opt)
{
    char *buf;

    if (!opt->answer)
        return;
    buf = G_malloc(strlen(opt->key) + 1 + strlen(opt->answer) + 1);
    snprintf(buf, GPATH_MAX, "%s=%s", opt->key, opt->answer);
    new_argv[new_argc++] = buf;
}

