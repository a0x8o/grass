int divider(char *edge)
{
    int n;

    fprintf(stdout, "%s", edge);
    n = page_width - 2;
    while (--n >= 0)
        fprintf(stdout, "-");
    fprintf(stdout, "%s", edge);
    newline();

    return 0;
}

static int max(int a, int b)
{
    return a > b ? a : b;
}

int trailer(void)
{
    divider("+");
    while (nlines > 0)
        newline();
    if (use_formfeed)
        fprintf(stdout, "\f");

    return 0;
}

int newline(void)
{
    fprintf(stdout, "\n");
    nlines--;

    return 0;
}

int lcr(const char *left, const char *center, const char *right, char *buf,
        int n)
{
    int ll, lc, lr;
    int sc, sr;

    ll = strlen(left);
    lc = strlen(center);
    lr = strlen(right);

    sc = (n - lc) / 2 - ll;
    sr = n - lr - lc - (n - lc) / 2;

    sprintf(buf, "%s%*s%s%*s%s", left, sc > 0 ? sc : 0, "", center,
            sc > 0 ? sr : sr + sc, "", right);

    return 0;
}
