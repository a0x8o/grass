template <class T>
void printStream(ostream &s, AMI_STREAM<T> *str)
{
    T *elt;
    AMI_err ae;

    str->seek(0);
    while ((ae = str->read_item(&elt)) == AMI_ERROR_NO_ERROR) {
        s << *elt << endl;
    }
    str->seek(0);
}

