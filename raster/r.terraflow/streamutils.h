template <class T, class FUN>
void printGridStream(AMI_STREAM<T> *str, dimension_type nrows,
                     dimension_type ncols, char *name, FUN fmt)
{
    T *elt;
    AMI_err ae;
    ofstream fstrm(name);

    if (stats)
        stats->recordLength("saving grid", str);
    fstrm << "rows=" << nrows << endl;
    fstrm << "cols=" << ncols << endl;

    assert(str->stream_len() == (off_t)nrows * ncols);
    str->seek(0);
    for (dimension_type i = 0; i < nrows; i++) {
        for (dimension_type j = 0; j < ncols; j++) {
            ae = str->read_item(&elt);
            assert(ae == AMI_ERROR_NO_ERROR);
            fstrm << " " << fmt(*elt);
        }
        fstrm << endl;
    }
    str->seek(0);
}

