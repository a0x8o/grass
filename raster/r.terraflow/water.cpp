int compressedWaterWindowBaseType::computeDelta(waterWindowBaseType *center,
                                                int index,
                                                waterWindowBaseType *p) const
{
    if (center->el != p->el) {
        assert(p->depth == 1 || center->el > p->el);
        return 0;
    }
    if (index > 7)
        return 0; /* we store our depth elsewhere */

    int d = p->depth - center->depth + 1;
    assert(d >= 0);
#ifndef NDEBUG
    if (d > 2) {
        cerr << "whoops - assertion failure" << endl;
        cerr << "center = " << *center << endl;
        cerr << "p = " << *p << endl;
        cerr << "this = " << *this << endl;
    }
#endif
    assert(d <= 2);
    return d << (2 * index);
}

compressedWaterWindowBaseType::compressedWaterWindowBaseType(
    dimension_type gi, dimension_type gj, waterWindowBaseType *a,
    waterWindowBaseType *b, waterWindowBaseType *c)
    : ijBaseType(gi, gj)
{

    for (int i = 0; i < 3; i++) {
        el[i] = a[i].el;
        el[i + 3] = b[i].el;
        el[i + 6] = c[i].el;
    }

    for (int i = 0; i < 3; i++) {
        const direction_type mask_a[] = {2, 4, 8};
        const direction_type mask_b[] = {1, 0, 16};
        const direction_type mask_c[] = {128, 64, 32};
        points.setBit(i, a[i].dir & mask_a[i]);
        points.setBit(norm(i + 3), b[i].dir & mask_b[i]);
        points.setBit(i + 5, c[i].dir & mask_c[i]);
    }
    dir = b[1].dir;
    depth = b[1].depth;
    depth_delta = 0;

    /* nodata is not processed. */
    if (is_nodata(b[1].el)) {
        return;
    }

    for (int i = 0; i < 3; i++) {
        depth_delta |= computeDelta(b + 1, norm(-1, i - 1), a + i);
        depth_delta |= computeDelta(b + 1, norm(0, i - 1), b + i);
        depth_delta |= computeDelta(b + 1, norm(1, i - 1), c + i);
    }
}

fillPriority compressedWaterWindowBaseType::getPriority() const
{
    return fillPriority(getElevation(), getDepth(), i, j);
}

bfs_depth_type compressedWaterWindowBaseType::getDepth(int k) const
{
    if (getElevation() != getElevation(k))
        return DEPTH_INITIAL;
    return depth + ((depth_delta >> (norm(k) * 2)) & 0x3) - 1;
}

void compressedWaterWindowBaseType::sanityCheck()
{
    assert(i >= -1);
    assert(j >= -1);
    assert(depth >= DEPTH_INITIAL);
}

