/* smaller elevation, depth is smaller priority */
int fillPriority::compare(const fillPriority &a, const fillPriority &b)
{
    if (a.el < b.el)
        return -1;
    if (a.el > b.el)
        return 1;

    if (a.depth < b.depth)
        return -1;
    if (a.depth > b.depth)
        return 1;

    if (a.i < b.i)
        return -1;
    if (a.i > b.i)
        return 1;

    if (a.j < b.j)
        return -1;
    if (a.j > b.j)
        return 1;

    return 0;
}

int fillPriority::qscompare(const void *a, const void *b)
{
    fillPriority *x = (fillPriority *)a;
    fillPriority *y = (fillPriority *)b;
    return compare(*x, *y);
}

int operator<(const fillPriority &a, const fillPriority &b)
{
    if (a.el < b.el)
        return 1;
    if (a.el > b.el)
        return 0;

    if (a.depth < b.depth)
        return 1;
    if (a.depth > b.depth)
        return 0;

    if (a.i < b.i)
        return 1;
    if (a.i > b.i)
        return 0;

    if (a.j < b.j)
        return 1;
    if (a.j > b.j)
        return 0;

    return 0;
}

int operator<=(const fillPriority &a, const fillPriority &b)
{
    if (a.el < b.el)
        return 1;
    if (a.el > b.el)
        return 0;

    if (a.depth < b.depth)
        return 1;
    if (a.depth > b.depth)
        return 0;

    if (a.i < b.i)
        return 1;
    if (a.i > b.i)
        return 0;

    if (a.j < b.j)
        return 1;
    if (a.j > b.j)
        return 0;

    return 1;
}

int operator>(const fillPriority &a, const fillPriority &b)
{
    if (a.el < b.el)
        return 0;
    if (a.el > b.el)
        return 1;

    if (a.depth < b.depth)
        return 0;
    if (a.depth > b.depth)
        return 1;

    if (a.i < b.i)
        return 0;
    if (a.i > b.i)
        return 1;

    if (a.j < b.j)
        return 0;
    if (a.j > b.j)
        return 1;

    return 0;
}

int operator==(const fillPriority &a, const fillPriority &b)
{
    return (a.el == b.el) && (a.depth == b.depth) && (a.i == b.i) &&
           (a.j == b.j);
}

int operator!=(const fillPriority &a, const fillPriority &b)
{
    return (a.el != b.el) || (a.depth != b.depth) || (a.i != b.i) ||
           (a.j != b.j);
}

