/*if occurred an error returns NULL */
char *concatena(const char *str1, const char *str2)
{
    char *conc = (char *)G_malloc(strlen(str1) + strlen(str2) + 1);

    if (conc == NULL) {
        return NULL;
    }
    strcpy(conc, str1);
    strcat(conc, str2);
    return conc;
}

